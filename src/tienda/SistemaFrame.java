    private void btnAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltaActionPerformed
        
        int codigo = Integer.valueOf(cajaCodigo.getText());
        String nombre = cajaNombre.getText();
        float precio = Float.valueOf(cajaPrecio.getText()); 
        int Stock = Integer.valueOf(cajaStock.getText());
        int cantidad = 0;
        
        Producto p1 = new Producto(codigo,nombre, precio, Stock, cantidad);
        
        
            p1.alta();
            consultarProductos();
            cajaCodigo.setText(""); 
            cajaNombre.setText("");
            cajaPrecio.setText(""); 
            cajaStock.setText("");

    }//GEN-LAST:event_btnAltaActionPerformed

    private void btnBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajaActionPerformed
          
        int i = Integer.valueOf(cajaCodigo.getText());
        String nombreProducto = new Producto(0,"",0,0,0).buscar(i);
        
        if (nombreProducto == null){
            JOptionPane.showMessageDialog(this, "Producto no encontrado");
        }else{
            int opcion = JOptionPane.showConfirmDialog(this, "Â¿EstÃ¡s seguro de eliminar el producto "+nombreProducto+"?","",JOptionPane.YES_NO_OPTION);
            
            if(opcion == 0){
                new Producto(0,"",0,0,0).baja(i);
                JOptionPane.showMessageDialog(this, "Producto eliminado");
                cajaCodigo.setText(""); 
                cajaNombre.setText("");
                cajaPrecio.setText(""); 
                cajaStock.setText(""); 
                consultarProductos();
            }
        }
    }//GEN-LAST:event_btnBajaActionPerformed

    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        int fila = tableProducto.getSelectedRow();
        
        if(fila!=-1){
            cajaCodigo.setText((String)tableProducto.getValueAt(fila,0));
            CodigoModificar = Integer.parseInt(cajaCodigo.getText());
            cajaNombre.setText((String)tableProducto.getValueAt(fila,1));
            cajaPrecio.setText((String)tableProducto.getValueAt(fila,2));
            cajaStock.setText((String)tableProducto.getValueAt(fila,3));
            btnModificar.setEnabled(true);
        }else
            JOptionPane.showMessageDialog(this, "Debes seleccionar una fila de la tabla");
    }//GEN-LAST:event_btnCargarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
            
        int codigo = Integer.valueOf(cajaCodigo.getText());
        String nombre = cajaNombre.getText();
        float precio = Float.valueOf(cajaPrecio.getText()); 
        int Stock = Integer.valueOf(cajaStock.getText());
        
        Producto p = new Producto(codigo,nombre, precio, Stock, 0);
        
        
            p.modificar(CodigoModificar);
            consultarProductos();
            cajaCodigo.setText(""); 
            cajaNombre.setText("");
            cajaPrecio.setText(""); 
            cajaStock.setText("");
        btnModificar.setEnabled(false);
    }//GEN-LAST:event_btnModificarActionPerformed

